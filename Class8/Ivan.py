# Task: Make a function that checks if a number is even or not. If the number is even, return True. If the number is odd, return False.

def is_even(input):
    if input % 2 == 0:
        return True
    else:
        return False

#################################################

if (is_even(2)):
    print("It is even")
else:
    print("It is odd")


